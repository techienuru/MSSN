          <p class="text-justify">
            The Muslim Students' Society of Nigeria (MSSN) was founded on
            April 18, 1954, and officially launched on May 30, 1954, at the
            Ansaruddeen Alakoro Mosque School Hall in Lagos. Spearheaded by
            students Tajudeen Adisa Aromoshodu, Sumola Akin Luguda, and
            Abdurrahman Alade Sahid, its creation was driven by the need to
            protect Muslim students from forced religious conversions and name
            changes in Christian missionary schools. MSSN aimed to safeguard
            Islamic identity and culture amidst an educational environment
            dominated by Christian evangelism.
          </p>
          <p class="text-justify">
            The society sought to provide a platform for Muslim students to
            engage in organized religious activities and Da'awa (Islamic
            propagation). Within a year of its formation, MSSN established
            branches in schools across Lagos, Abeokuta, Ibadan, Ilesha,
            Ijebu-Ode, and Ikorodu. Two years later, its presence had expanded
            to virtually all government schools in Northern Nigeria.
